def initialize_embeddings():
    """Инициализация модели и предварительное вычисление embeddings"""
    global model, phrases_list, phrases_embeddings
    
    logger.info("Загрузка модели sentence-transformers...")
    start_time = time.time()
    
    try:
        # Используем легкую многоязычную модель
        model = SentenceTransformer('paraphrase-multilingual-MiniLM-L12-v2')
        
        # Конвертируем set в list для индексации
        phrases_list = list(phrases_db)
        
        logger.info(f"Вычисление embeddings для {len(phrases_list)} фраз...")
        # Предварительно вычисляем embeddings для всех фраз
        phrases_embeddings = model.encode(phrases_list, convert_to_tensor=False)
        
        load_time = time.time() - start_time
        logger.info(f"Инициализация завершена за {load_time:.2f} секунд")
        
    except Exception as e:
        logger.error(f"Ошибка при инициализации: {e}")
        raise

@asynccontextmanager
async def lifespan(app: FastAPI):
    # Startup
    logger.info("Запуск приложения...")
    initialize_embeddings()
    yield
    # Shutdown
    logger.info("Завершение работы приложения...")

app = FastAPI(title="Phrase Checker with Embeddings", version="2.0.0", lifespan=lifespan)

