def find_most_similar(query_text: str, threshold: float = 0.8) -> Tuple[bool, float, str]:
    """Находит наиболее похожую фразу"""
    if model is None or phrases_embeddings is None:
        raise HTTPException(status_code=500, detail="Модель не инициализирована")
    
    # Вычисляем embedding для запроса
    query_embedding = model.encode([query_text], convert_to_tensor=False)
    
    # Вычисляем косинусное сходство
    similarities = cosine_similarity(query_embedding, phrases_embeddings)[0]
    
    # Находим максимальное сходство
    max_similarity_idx = np.argmax(similarities)
    max_similarity = similarities[max_similarity_idx]
    
    # Проверяем порог
    if max_similarity >= threshold:
        matched_phrase = phrases_list[max_similarity_idx]
        return True, float(max_similarity), matched_phrase
    else:
        return False, float(max_similarity), ""

