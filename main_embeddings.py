    # Вычисляем embedding для запроса
    query_embedding = model.encode([query_text], convert_to_tensor=False)
    
    # Вычисляем косинусное сходство
    similarities = cosine_similarity(query_embedding, phrases_embeddings)[0]
    
    # Получаем топ-K индексов
    top_indices = np.argsort(similarities)[::-1][:top_k]
    
    # Формируем результат
    results = []
    for idx in top_indices:
        phrase = phrases_list[idx]
        similarity = float(similarities[idx])
        results.append((phrase, similarity))
    
    return results

